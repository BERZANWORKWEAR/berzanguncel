const seededProducts = [...ydsProducts, ...threeMProducts, ...portwestProducts];

function isSeededProduct(product) {
  return PREFIXES.some((prefix) => String(product?.id || "").startsWith(prefix));
}

async function syncDb(dbPath) {
  const raw = await fs.readFile(dbPath, "utf8");
  const db = JSON.parse(raw);
  db.products = [
    ...(Array.isArray(db.products) ? db.products.filter((product) => !isSeededProduct(product)) : []),
    ...seededProducts,
  ];
  db.meta = db.meta || {};
  db.meta.updated_at = new Date().toISOString();
  await fs.writeFile(dbPath, `${JSON.stringify(db, null, 2)}\n`, "utf8");
}

